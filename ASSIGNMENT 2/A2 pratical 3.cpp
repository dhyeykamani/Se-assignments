#include<stdio.h>

float pi=3.14;// constant

int main(void) {
    // declarations of variable
    int age = 19;               // Integer variable
    char firstletter = 'D';         // Character variable
    float height = 5.8;        // Float variable

    // Displaying the values
    printf("Age is: %d\n", age);
    printf("name start with: %c\n",firstletter);
    printf("Height: %f\n", height);
    printf("Value of PI: %f\n", pi);

    return 0;  
}

